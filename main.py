x = "champion"

# print the first 4 characters
print(x[0:4])

# print index number 5-7
print(x[5:8])

# print from index number 4 to the last charcter
print(x[4: ])

# print the first 5 charcters
print(x[0:5])
print(x[ :5])

#print the last 5 characters
print(x[3:])

# print every alternate character
print(x[ : : 2])

# print the string in reverse
